####Reletional operator

#1. == (Exact equals to)
print(10==10)
print(10=='10')

#2. != (not equals to )
print(10 !='10')
print(10 !=10)

#3. > (Greater than)
print(10>2)
print(10>20)

#4.<(less than)
print(10<20)
print(10<2)

#5.>= (greater than or equals to)
print(10>=10)
print(10>=6)
print(10>=20)

#6.<=(less than equal to)
print(10<=20)
print(10<=10)
print(10<=2)