#1. & (AND)
#If both bits are 1 then consider 1 otherwise 0
print(13&6)

#2. | (OR)
#If  both bits are 0 then consider  0 otherwise 1
print( 13 | 6)

#3. ^(XOR)
#If both bits are different the consider 1 otherwise 0
print(13^6)

#4.~ (NOT)
print(~10)    # -(10+1)
print(~5)     #-(5+1)

#5. << (Right shift)
print(34<<4)
print(5<<2)
print(4<<3)

#6. >> ( Left shift)
print(45>>5)
print(23>>3)