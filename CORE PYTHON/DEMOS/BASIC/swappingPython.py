
####swapping using two variables
x = 10
y = 20

print(f'Before swapping  x is {x} and y is {y}')

x,y = y,x

print(f'After swapping  x is {x} and y is {y}')

####swapping using third variable
x = 30
y = 50
z = 0
print(f'Before swapping x is {x} and y is {y}')


z = x
x = y
y = z
print(f'After swapping x is {x} and y is {y}')